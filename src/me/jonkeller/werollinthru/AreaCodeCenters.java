    public static int getNearest(Location location) {
        double nearestDistanceSquared = Double.MAX_VALUE;
        int npa = -1;
        Coordinates locationCoordinates = new Coordinates(location.getLatitude(), location.getLongitude());
        for (Entry<Integer, Coordinates> entry : areaCodeCenters.entrySet()) {
            double distanceSquared = locationCoordinates.squareOfDistance(entry.getValue());
            if (distanceSquared < nearestDistanceSquared) {
                nearestDistanceSquared = distanceSquared;
                npa = entry.getKey();
            }
        }
        return npa;
    }

